
def Addition(num1, num2):
    add = num1 + num2
    print(add)
    return Addition

def Substraction(num1, num2):
    sub = num1 - num2
    print(sub)
    return Substraction
def Multiplication(num1, num2):
    mul = num1 * num2
    print(mul)
    return Multiplication
def Division(num1, num2):
    div = num1 / num2
    print(div)
    return Division

